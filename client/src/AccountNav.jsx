import { Link, useLocation } from "react-router-dom";
import { useTranslation } from 'react-i18next';

export default function AccountNav() {
  const { pathname } = useLocation();
  // a constant for the translation
  const { t } = useTranslation();

  let subpage = pathname.split("/")?.[2]; //?. means that it can sometimes be empty
  // If subpage is undefined (i.e., the URL doesn't have a third segment), default to 'profile'
  if (subpage === undefined) {
    subpage = "profile";
  }
  //checks which subpage we are at and changes the links appearance
  function linkClasses(type = null) {
    let classes = "inline-flex gap-1 py-2 px-6";
    if (type === subpage) {
      classes = "inline-flex gap-1 py-2 px-4 bg-primary text-white rounded-xl";
    } else {
      classes = "inline-flex gap-1 py-2 px-4 bg-gray-200 rounded-xl";
    }
    return classes;
  }
  return (
    <nav className="w-full flex justify-center mt-8 gap-6 mb-8">
      <div className="grid grid-cols-2 gap-4 lg:grid-cols-4">
        <Link className={linkClasses("profile")} to={"/account"}>
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth={1.5}
            stroke="currentColor"
            className="w-6 h-6"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M15.75 6a3.75 3.75 0 11-7.5 0 3.75 3.75 0 017.5 0zM4.501 20.118a7.5 7.5 0 0114.998 0A17.933 17.933 0 0112 21.75c-2.676 0-5.216-.584-7.499-1.632z"
            />
          </svg>
          {t('MyProfile')}
        </Link>
        <Link className={linkClasses("liked")} to={"/account/liked"}>
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth={1.5}
            stroke="currentColor"
            className="w-6 h-6"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M21 8.25c0-2.485-2.099-4.5-4.688-4.5-1.935 0-3.597 1.126-4.312 2.733-.715-1.607-2.377-2.733-4.313-2.733C5.1 3.75 3 5.765 3 8.25c0 7.22 9 12 9 12s9-4.78 9-12Z"
            />
          </svg>
          {t('Liked')}
        </Link>
        <Link className={linkClasses("bookings")} to={"/account/bookings"}>
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth={1.5}
            stroke="currentColor"
            className="w-6 h-6"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M17.593 3.322c1.1.128 1.907 1.077 1.907 2.185V21L12 17.25 4.5 21V5.507c0-1.108.806-2.057 1.907-2.185a48.507 48.507 0 0111.186 0z"
            />
          </svg>
          {t('MyBookings')}
        </Link>
        <Link className={linkClasses("places")} to={"/account/places"}>
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth={1.5}
            stroke="currentColor"
            className="w-6 h-6"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M8.25 21v-4.875c0-.621.504-1.125 1.125-1.125h2.25c.621 0 1.125.504 1.125 1.125V21m0 0h4.5V3.545M12.75 21h7.5V10.75M2.25 21h1.5m18 0h-18M2.25 9l4.5-1.636M18.75 3l-1.5.545m0 6.205l3 1m1.5.5l-1.5-.5M6.75 7.364V3h-3v18m3-13.636l10.5-3.819"
            />
          </svg>
          {t('MyAccomodations')}
        </Link>
      </div>
    </nav>
  );
}
